package com.techpp.modal;

public class Rider {
	
	private String mobileNumber;
	private String groupCode;
	private String typeOfRide;
	private String srcLat;
	private String srcLong;
	private String destLat;
	private String destLong;
	private String riderId;
	private String rideStartTime;
	private String rideStatus;


	
	public String getRideStatus() {
		return rideStatus;
	}

	public void setRideStatus(String rideStatus) {
		this.rideStatus = rideStatus;
	}

	public String getRiderId() {
		return riderId;
	}

	public void setRiderId(String riderId) {
		this.riderId = riderId;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	public String getGroupCode() {
		return groupCode;
	}

	public void setGroupCode(String groupCode) {
		this.groupCode = groupCode;
	}

	public String getTypeOfRide() {
		return typeOfRide;
	}

	public void setTypeOfRide(String typeOfRide) {
		this.typeOfRide = typeOfRide;
	}

	public String getSrcLat() {
		return srcLat;
	}

	public void setSrcLat(String srcLat) {
		this.srcLat = srcLat;
	}

	public String getSrcLong() {
		return srcLong;
	}

	public void setSrcLong(String srcLong) {
		this.srcLong = srcLong;
	}

	public String getDestLat() {
		return destLat;
	}

	public void setDestLat(String destLat) {
		this.destLat = destLat;
	}

	public String getDestLong() {
		return destLong;
	}

	public void setDestLong(String destLong) {
		this.destLong = destLong;
	}

	public String getRideStartTime() {
		return rideStartTime;
	}

	public void setRideStartTime(String rideStartTime) {
		this.rideStartTime = rideStartTime;
	}
	
	
	
		
}
